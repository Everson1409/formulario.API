export class Usuario {
    constructor(nome, cep, logradouro, bairro, cidade, estado, numero) {
        this.nome = nome;
        this.cep = cep;
        this.logradouro = logradouro;
        this.bairro = bairro;
        this.cidade = cidade;
        this.estado = estado;
        this.numero = numero;
    }
}

